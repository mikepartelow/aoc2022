import heapq
from lib import assert_answer


def find_start_and_end(hmap):
    """Return (y,x) coordinates of start, end marks on the hmap."""
    start, end = (-1, -1), (-1, -1)

    for y, row in enumerate(hmap.splitlines()):
        for x, square in enumerate(row):
            if square == "S":
                assert start == (-1, -1)
                start = (y, x)
                if end != (-1, -1):
                    return start, end
            elif square == "E":
                assert end == (-1, -1)
                end = (y, x)
                if start != (-1, -1):
                    return start, end
    assert False, "darn"


def solve(hmap, part2=False):
    """Solve it."""

    # use Dijkstra's algo to compute the length of the shortest path
    # from end to start (not start to end!)
    heights = []
    for row in hmap.splitlines():
        heights.append([ord(c) for c in row])

    rows, cols = len(heights), len(heights[0])
    start, end = find_start_and_end(hmap)

    heights[start[0]][start[1]] = ord("a")
    heights[end[0]][end[1]] = ord("z")

    visited = set()
    walk = [(0, end)]  # effort, (y, x)

    while walk:
        effort, (y, x) = heapq.heappop(walk)

        if part2:
            if chr(heights[y][x]) == "a":
                return effort
        else:
            if (y, x) == start:
                return effort

        if (y, x) in visited:
            continue

        visited.add((y, x))

        for y1, x1 in ((y, x + 1), (y, x - 1), (y + 1, x), (y - 1, x)):
            if 0 <= y1 < rows and 0 <= x1 < cols:
                if heights[y][x] - heights[y1][x1] <= 1:
                    heapq.heappush(walk, (1 + effort, (y1, x1)))

    return 0


def part_one(filename):
    """Part 1"""
    return solve(filename)


def part_two(filename):
    """Part 2"""
    return solve(filename, part2=True)


